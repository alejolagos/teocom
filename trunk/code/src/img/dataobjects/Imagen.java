package img.dataobjects;

public class Imagen {
	private String nombre;
	private double[] imagen;
	
	
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public double[] getImagen() {
		return imagen;
	}
	public void setImagen(double[] imagen) {
		this.imagen = imagen;
	}
}
